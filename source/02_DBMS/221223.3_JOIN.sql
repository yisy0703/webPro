    -- 탄탄ex3. 직급이 MANAGER인 사원의 이름, 부서명을 출력하시오
SELECT ENAME, DNAME FROM EMP E, DEPT D WHERE E.DEPTNO=D.DEPTNO AND JOB='MANAGER';
    -- 탄탄ex4. Comm이 null이 아닌 사원의 이름, 급여, 부서코드, 근무지를 출력하시오.
