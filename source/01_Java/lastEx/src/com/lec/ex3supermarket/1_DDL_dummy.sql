-- ♠ 테이블&시퀀스 생성
CREATE TABLE CUS_LEVEL(
  LEVELNO NUMBER(1,0),
  LEVELNAME VARCHAR2(20),
  LOW NUMBER(9,0),
  HIGH NUMBER(9,0),
  PRIMARY KEY(LEVELNO)
);
CREATE SEQUENCE CUSTOMER_CID_SEQ 
  MAXVALUE 999999 NOCACHE NOCYCLE;
CREATE TABLE CUSTOMER (
  CID NUMBER(6,0),
  CTEL VARCHAR2(20),
  CNAME VARCHAR2(30),
  CPOINT NUMBER(9,0) DEFAULT 1000,
  CAMOUNT NUMBER(9) DEFAULT 0,
  LEVELNO NUMBER(1) DEFAULT 1 NOT NULL,
  PRIMARY KEY(CID),
  FOREIGN KEY(LEVELNO) REFERENCES CUS_LEVEL(LEVELNO)
);
